    public new_info(Member member, int index) {
        this.editingMember = member;
        this.memberIndex = index;
        initComponents();
        
        pac_ch.removeAllItems();
        mem_ch.removeAllItems();

        pac_ch.addItem("Normal");
        pac_ch.addItem("Premium");
        pac_ch.addItem("Celebrity");

        mem_ch.addItem("1 Month");
        mem_ch.addItem("3 Month");
        mem_ch.addItem("6 Month");
        mem_ch.addItem("1 Year");

        loadMemberData();
    }

    private void loadMemberData() {
        name_txt.setText(editingMember.getName());
        email_txt.setText(editingMember.getEmail());
        phone_txt.setText(editingMember.getPhone());
        loc_txt.setText(editingMember.getLocation());
        pac_ch.setSelectedItem(editingMember.getPackage());
        mem_ch.setSelectedItem(editingMember.getMembership());

        System.out.println("Index of the selected member: " + memberIndex);
    }

    public void setMemberIndex(int index) {
        this.memberIndex = index;
    }


    private void confirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_confirmActionPerformed
        // TODO add your handling code here:
        editingMember.setName(name_txt.getText());
        editingMember.setEmail(email_txt.getText());
        editingMember.setPhone(phone_txt.getText());
        editingMember.setLocation(loc_txt.getText());
        editingMember.setPackage((String) pac_ch.getSelectedItem());
        editingMember.setMembership((String) mem_ch.getSelectedItem());
        
        boolean updated = mem.updateMember(editingMember);
    
        if (updated) {
            JOptionPane.showMessageDialog(this, "Update successful!");
            Main_Menu main = new Main_Menu();
            main.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "Update failed. Please try again.");
        }
    }//GEN-LAST:event_confirmActionPerformed

